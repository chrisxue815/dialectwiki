package org.dw.model;

public class Dialect {

	private String dialect_id;
	private String dialect_name;
	private String dialect_intro;
	
	public Dialect() {
		
	}

	public String getDialect_id() {
		return dialect_id;
	}

	public void setDialect_id(String dialectId) {
		dialect_id = dialectId;
	}

	public String getDialect_name() {
		return dialect_name;
	}

	public void setDialect_name(String dialectName) {
		dialect_name = dialectName;
	}

	public String getDialect_intro() {
		return dialect_intro;
	}

	public void setDialect_intro(String dialectIntro) {
		dialect_intro = dialectIntro;
	}
	
	
	
	

}
